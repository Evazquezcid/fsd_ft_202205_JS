// OPERADORES ARITMETICOS

// SUMA -> +
// RESTA -> -
// MULTIPLICACION -> *
// DIVISION -> /
// RESTO -> %

const numA = 5;
const numB = 6;

console.log("SUMA ->", numA + numB);
console.log("RESTA ->", numA - numB);
console.log("MULTIPLICACION ->", numA * numB);
console.log("DIVISION ->", numA / numB);
console.log("REST ->", numA % numB);
